categorySelect1.addEventListener('change', function () {
    const selectedCategory1 = this.value;

    // Clear previous subcategories1
    subcategorySelect1.innerHTML = '<option>-- Select Sub Category --</option>';

    // If a valid category is selected, populate the subcategory dropdown
    if (subcategories1[selectedCategory1]) {
        subcategories1[selectedCategory1].forEach(function (subcategory1) {
            const option1 = document.createElement('option');
            option1.textContent = subcategory1;
            subcategorySelect1.appendChild(option1);
        });
    }
});

